function flatten(prefix: string, tree: MsgTree, out: Record<string, string>): void {
  for (const [k, v] of Object.entries(tree)) {
    const path = prefix ? `${prefix}.${k}` : k
    if (typeof v === 'string') out[path] = v
    else flatten(path, v as MsgTree, out)
  }
}

